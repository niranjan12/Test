package com.model;

public class Outstandings {
	private int id;
	private String outstandings;
	private String reason;
	private long osamount;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOutstandings() {
		return outstandings;
	}
	public void setOutstandings(String outstandings) {
		this.outstandings = outstandings;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public long getOsamount() {
		return osamount;
	}
	public void setOsamount(long osamount) {
		this.osamount = osamount;
	}
	
	

}
